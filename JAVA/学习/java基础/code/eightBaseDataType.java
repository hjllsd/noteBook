public class eightBaseDataType {
    public static void main(String[] args) {
         //1.定义byte类型的变量
        //数据类型 变量名 = 数据值;
        byte b = 10;
        System.out.println(b);

        //2.定义short类型的变量
        short s = 20;
        System.out.println(s);

        //3.定义int类型的变量
        int i = 30;
        System.out.println(i);

        //4.定义long类型的变量
        long l = 40L;
        System.out.println(l);

        //5.定义float类型的变量
        float f = 50.5F;
        System.out.println(f);

        //6.定义double类型的变量
        double d = 60.6;
        System.out.println(d);

        //7.定义char类型的变量
        char ch = 'a';
        System.out.println(ch);

        //8.定义boolean类型的变量
        boolean flag = true;
        System.out.println(flag);
    }
    
}
